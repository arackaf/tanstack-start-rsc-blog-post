import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT = path.join(__dirname, "../src/manual-icons/bugSvgBloat.tsx");

/** Deterministic pseudorandom helper for repeatable “buggy” geometry */
function scramble(n, salt) {
  let x = (n * 0x9e3779b1 + salt) >>> 0;
  return () => {
    x ^= x << 13;
    x ^= x >>> 17;
    x ^= x << 5;
    return (x >>> 0) / 4294967296;
  };
}

function buildPath(rand, complexity) {
  let d = "M ";
  let cx = rand() * 360 + 20;
  let cy = rand() * 360 + 20;
  d += `${cx.toFixed(2)},${cy.toFixed(2)} `;
  for (let i = 0; i < complexity; i++) {
    const op = i % 5;
    if (op === 0) {
      cx += (rand() - 0.5) * 80;
      cy += (rand() - 0.5) * 80;
      d += `L ${cx.toFixed(2)},${cy.toFixed(2)} `;
    } else if (op === 1) {
      const x1 = cx + (rand() - 0.5) * 60;
      const y1 = cy + (rand() - 0.5) * 60;
      cx += (rand() - 0.5) * 70;
      cy += (rand() - 0.5) * 70;
      d += `Q ${x1.toFixed(2)},${y1.toFixed(2)} ${cx.toFixed(2)},${cy.toFixed(2)} `;
    } else if (op === 2) {
      const x1 = cx + (rand() - 0.5) * 50;
      const y1 = cy + (rand() - 0.5) * 50;
      const x2 = cx + (rand() - 0.5) * 90;
      const y2 = cy + (rand() - 0.5) * 90;
      cx = (cx + x2) / 2;
      cy = (cy + y2) / 2;
      d += `C ${x1.toFixed(2)},${y1.toFixed(2)} ${x2.toFixed(2)},${y2.toFixed(2)} ${cx.toFixed(2)},${cy.toFixed(2)} `;
    } else if (op === 3) {
      const rx = rand() * 25 + 3;
      const ry = rand() * 18 + 2;
      const rot = rand() * 180;
      d += `A ${rx.toFixed(2)},${ry.toFixed(2)} ${rot.toFixed(
        2,
      )} 1,1 ${(cx + (rand() - 0.5) * rx).toFixed(2)},${(cy + (rand() - 0.5) * ry).toFixed(2)} `;
    } else {
      const ex = cx + (rand() - 0.5) * 100;
      const ey = cy + (rand() - 0.5) * 100;
      d += `L ${ex.toFixed(2)},${ey.toFixed(2)} Z M ${cx.toFixed(2)},${cy.toFixed(2)} `;
    }
  }
  d += "Z";
  return d;
}

let file = `// AUTO-GENERATED by scripts/generate-bug-svg-bloat.mjs — do not hand-edit.\n`;
file += `import type { SVGProps } from "react";\n\n`;
file += `export type BugBloatSvgProps = SVGProps<SVGSVGElement>;\n\n`;

/** Target ~1 MiB TSX total (still plenty of defs + geometry). */
const PATHS_PER_SVG = 12;
const GRADIENTS = 6;
const PATH_COMPLEXITY = 9;

for (let n = 1; n <= 100; n++) {
  const name = `BugSvgBloat${String(n).padStart(3, "0")}`;
  const randPaths = scramble(n * 9973 + 31337, n);
  const randDefs = scramble(n * 7919 + 424242, n);

  file += `export function ${name}(props: BugBloatSvgProps) {\n`;
  file += `  return (\n`;
  file += `    <svg role="img" aria-label="${name}: decorative bug-density SVG blob" width="240" height="240" viewBox="0 0 400 400" xmlns="http://www.w3.org/2000/svg" {...props}>\n`;
  file += `      <title>${name}</title>\n`;
  file += `      <defs>\n`;

  for (let g = 0; g < GRADIENTS; g++) {
    const r = randDefs();
    const id = `g${n}_${g}`;
    file += `        <linearGradient id="${id}" x1="${(r * 100).toFixed(2)}%" y1="${(randDefs() * 100).toFixed(
      2,
    )}%" x2="${(randDefs() * 100).toFixed(2)}%" y2="${(randDefs() * 100).toFixed(2)}%">\n`;
    file += `          <stop offset="0%" stopColor="#${Math.floor(randDefs() * 0xffffff)
      .toString(16)
      .padStart(6, "0")}" />\n`;
    file += `          <stop offset="35%" stopColor="#${Math.floor(randDefs() * 0xffffff)
      .toString(16)
      .padStart(6, "0")}" stopOpacity="${(randDefs() * 0.4 + 0.6).toFixed(4)}" />\n`;
    file += `          <stop offset="100%" stopColor="#${Math.floor(randDefs() * 0xffffff)
      .toString(16)
      .padStart(6, "0")}" />\n`;
    file += `        </linearGradient>\n`;

    const rid = `rg${n}_${g}`;
    file += `        <radialGradient id="${rid}" cx="${(randDefs() * 100).toFixed(
      2,
    )}%" cy="${(randDefs() * 100).toFixed(2)}%" r="${(randDefs() * 80 + 20).toFixed(2)}%">\n`;
    file += `          <stop offset="0%" stopColor="#${Math.floor(randDefs() * 0xffffff)
      .toString(16)
      .padStart(6, "0")}" stopOpacity="${(randDefs() * 0.5 + 0.3).toFixed(4)}" />\n`;
    file += `          <stop offset="100%" stopColor="#${Math.floor(randDefs() * 0xffffff)
      .toString(16)
      .padStart(6, "0")}" />\n`;
    file += `        </radialGradient>\n`;

    const fid = `flt${n}_${g}`;
    file += `        <filter id="${fid}" x="-20%" y="-20%" width="140%" height="140%">\n`;
    file += `          <feGaussianBlur stdDeviation="${(randDefs() * 1.8 + 0.2).toFixed(4)}" />\n`;
    file += `        </filter>\n`;
  }

  file += `      </defs>\n`;
  file += `      <rect x="0" y="0" width="400" height="400" fill="#0f1118" />\n`;

  for (let p = 0; p < PATHS_PER_SVG; p++) {
    const d = buildPath(randPaths, PATH_COMPLEXITY);
    const lg = `g${n}_${p % GRADIENTS}`;
    const rg = `rg${n}_${((p + 7) % GRADIENTS)}`;
    const fl = `flt${n}_${((p + 13) % GRADIENTS)}`;
    file += `      <path fill="url(#${lg})" stroke="#1a2233" strokeWidth="0.35" opacity="${(randPaths() * 0.25 + 0.75).toFixed(4)}" filter="url(#${fl})" d="${d}" />\n`;
    file += `      <circle cx="${((p * 17 + n * 41) % 380 + 10).toFixed(
      2,
    )}" cy="${((p * 31 + n * 19) % 380 + 10).toFixed(2)}" r="${(randPaths() * 4 + 0.8).toFixed(
      2,
    )}" fill="url(#${rg})" opacity="${(randPaths() * 0.4 + 0.55).toFixed(4)}"/>\n`;
  }

  file += `    </svg>\n`;
  file += `  );\n`;
  file += `}\n\n`;
}

file += `/** All bug-bloat components in insertion order — import this tree to punish the client bundle */\n`;
file += `export const BUG_BLOAT_SVG_LIST = [\n`;
for (let n = 1; n <= 100; n++) {
  const name = `BugSvgBloat${String(n).padStart(3, "0")}`;
  file += `  ${name},\n`;
}
file += `] as const;\n`;

fs.writeFileSync(OUT, file, "utf8");
console.log(`Wrote ${OUT}`);
